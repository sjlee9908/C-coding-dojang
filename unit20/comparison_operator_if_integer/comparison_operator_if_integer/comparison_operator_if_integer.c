#include <stdio.h>

int main()
{
    int num1 = 10;

    if (num1 == 10)
        printf("10입니다.\n");

    if (num1 != 5)
        printf("5가 아닙니다.\n");

    if (num1 > 10)
        printf("10보다 큽니다.\n");

    if (num1 < 10)
        printf("10보다 작습니다.\n");

    if (num1 >= 10)
        printf("10보다 크거나 같습니다.\n");

    if (num1 <= 10)
        printf("10보다 작거나 같습니다.\n");

    return 0;
}