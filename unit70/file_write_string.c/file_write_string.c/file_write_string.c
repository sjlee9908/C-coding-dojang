#define _CRT_SECURE_NO_WARNINGS    // fopen 보안 경고로 인한 컴파일 에러 방지
#include <stdio.h>     // fopen, fwrite, fclose 함수가 선언된 헤더 파일
#include <string.h>    // strlen 함수가 선언된 헤더 파일

int main() {
	char* s1 = "Hello, world!";
	FILE* fp = fopen("hello.txt", "w");

	fwrite(s1, strlen(s1), 1, fp);
	
	fclose(fp);
	return 0;
}